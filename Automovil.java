
package herencia;

public class Automovil {
    
    private double llanta;
    private String color;
    private String marca;
    private int año;
            

    public Automovil(double llanta, String color, String marca, int año) {
        this.llanta = llanta;
        this.color = color;
        this.marca = marca;
        this.año = año;
    }

    public double getLlanta() {
        return llanta;
    }

    public String getColor() {
        return color;
    }

    public String getMarca() {
        return marca;
    }

    public int getAño() {
        return año;
    }
    
    
            
            
    
    
    
}
