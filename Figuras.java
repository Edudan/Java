
package polimorfismo;

public class Figuras {
    
    public int base;
    public int altura;
   
    public Figuras(int base, int altura) {
        this.base = base;
        this.altura = altura;
      
    }

    public int getBase() {
        return base;
    }

    public int getAltura() {
        return altura;
    }

  
}
